namespace OpenMod.API.Prioritization
{
    /// <summary>
    /// Represents priority.
    /// </summary>
    public enum Priority
    {
        /// <summary>
        /// The lowest priority.
        /// </summary>
        Lowest,

        /// <summary>
        /// The low priority.
        /// </summary>
        Low,

        /// <summary>
        /// The normal priority. This is the default priority.
        /// </summary>
        Normal,

        /// <summary>
        /// The high priority.
        /// </summary>
        High,

        /// <summary>
        /// The highest priority.
        /// </summary>
        Highest
    }
}